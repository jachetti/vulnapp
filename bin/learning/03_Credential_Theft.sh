#!/bin/bash
# Learning Scenario 3: Credential Theft in Containers
# Concept: Credentials are stored differently, but goal is same
# Duration: 5 minutes

set +e
echo "════════════════════════════════════════════════════════════════"
echo "  LEARNING SCENARIO 3: Credential Theft in Containers"
echo "  Finding Secrets in New Places"
echo "════════════════════════════════════════════════════════════════"
echo ""

echo "📚 CONCEPT: Where Are Credentials in Containers?"
echo ""
echo "ENDPOINT PARALLEL (What SEs Already Know):"
echo "  • Windows: LSASS memory, SAM database, credential manager"
echo "  • Linux: /etc/shadow, SSH keys (~/.ssh/id_rsa)"
echo "  • Goal: Steal credentials for privilege escalation/lateral movement"
echo ""
echo "CONTAINER VERSION (New Locations):"
echo "  • Environment variables (DATABASE_PASSWORD=secret123)"
echo "  • Kubernetes secrets (mounted as files)"
echo "  • Config files (/app/config.yml)"
echo "  • Cloud metadata APIs (AWS, Azure, GCP credentials)"
echo ""
sleep 2

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "STAGE 1: Environment Variable Dump"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""
echo "[+] Searching environment variables for secrets..."
echo ""
env | grep -iE "password|secret|key|token|api" || echo "[i] No obvious secrets in env"
echo ""
echo "    SE Note: Developers often pass credentials via environment"
echo "    variables. It's convenient but insecure."
echo ""
echo "    Real examples we've seen:"
echo "      DB_PASSWORD=prod_db_2024!"
echo "      AWS_SECRET_ACCESS_KEY=wJalrXUtnFEMI/K7MDENG..."
echo "      STRIPE_API_KEY=sk_live_..."
echo ""
sleep 2

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "STAGE 2: Kubernetes Secrets"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""
echo "[+] Checking for Kubernetes service account token..."
if [ -f /var/run/secrets/kubernetes.io/serviceaccount/token ]; then
    echo "✓ Kubernetes token found!"
    echo ""
    echo "Token (first 100 characters):"
    cat /var/run/secrets/kubernetes.io/serviceaccount/token | cut -c1-100
    echo "... (truncated)"
    echo ""
    echo "    SE Note: This token provides API access to Kubernetes"
    echo ""
    echo "    What attacker can do:"
    echo "      • List all pods, services, secrets"
    echo "      • Create new pods (potentially privileged)"
    echo "      • Access other applications' secrets"
    echo "      • Deploy malware cluster-wide"
    echo ""
    echo "    Customer objection: 'But isn't this needed for the app?'"
    echo "    Response: 'Yes, but most apps get MORE permissions than"
    echo "     needed. This is why Kubernetes RBAC and least privilege"
    echo "     matter. Falcon helps you see when these tokens are abused.'"
else
    echo "[i] Not in Kubernetes pod"
fi
echo ""
sleep 3

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "STAGE 3: File System Credential Hunting"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""
echo "[+] Searching for SSH keys..."
find / -name "id_rsa" -o -name "*.pem" -o -name "*.key" 2>/dev/null | head -5 || echo "[i] No SSH keys found"
echo ""
echo "    SE Note: SSH keys provide passwordless access to other systems"
echo ""
sleep 2

echo "[+] Searching for configuration files..."
find /etc /app /opt -name "*.conf" -o -name "*.yml" -o -name "*.yaml" 2>/dev/null | head -10 || echo "[i] Limited configs found"
echo ""
echo "    SE Note: Config files often contain:"
echo "      • Database connection strings"
echo "      • API keys"
echo "      • Internal service URLs"
echo ""
sleep 2

echo "[+] Checking common credential locations..."
cat /etc/passwd | head -5
echo "... (truncated)"
echo ""
echo "    SE Note: /etc/passwd is world-readable"
echo "    Shows all users, helps plan privilege escalation"
echo ""
sleep 2

echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "STAGE 4: Detection Test"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
bash crowdstrike_test_high 2>/dev/null || echo "✓ Test trigger executed"

echo ""
echo "════════════════════════════════════════════════════════════════"
echo "  LEARNING SUMMARY"
echo "════════════════════════════════════════════════════════════════"
echo ""
echo "KEY TAKEAWAYS for SEs:"
echo ""
echo "1. CREDENTIALS IN CONTAINERS:"
echo "   Environment variables > Kubernetes secrets > Config files"
echo ""
echo "2. KUBERNETES TOKENS ARE GOLD:"
echo "   • Automatically provided to every pod"
echo "   • Often over-permissioned"
echo "   • Key to lateral movement in K8s"
echo ""
echo "3. CUSTOMER DEMO TALKING POINTS:"
echo "   'In the Capital One breach, attackers stole AWS credentials"
echo "    from a container and used them to access 100 million records."
echo "    Falcon would have detected the credential theft in real-time.'"
echo ""
echo "📊 Falcon Detections: Credential Access, GenReverseShell"
echo "🎯 Next: Container Escape (THE key container-specific threat)"
echo ""
echo "════════════════════════════════════════════════════════════════"
